package com.mbi;

/**
 * Custom date time POJO.
 */
final class CustomDateTime {

    private int y;
    private int mo;
    private int d;
    private int h;
    private int m;
    private int s;

    public int getY() {
        return y;
    }

    public void setY(final int y) {
        this.y = y;
    }

    public int getMo() {
        return mo;
    }

    public void setMo(final int mo) {
        this.mo = mo;
    }

    public int getD() {
        return d;
    }

    public void setD(final int d) {
        this.d = d;
    }

    public int getH() {
        return h;
    }

    public void setH(final int h) {
        this.h = h;
    }

    public int getM() {
        return m;
    }

    public void setM(final int m) {
        this.m = m;
    }

    public int getS() {
        return s;
    }

    public void setS(final int s) {
        this.s = s;
    }
}
